#include <bits/stdc++.h>

using namespace std;

typedef long long int lli;
typedef pair<int, int> pii;
typedef pair<lli, lli> pll;

int gInt () {
    int i;
    scanf("%d", &i);
    return i;
}

lli gLong () {
    lli i;
    scanf("%lld", &i);
    return i;
}

double gDouble () {
    double i;
    scanf("%lf", &i);
    return i;
}

void init () {
    freopen("$INFILE", "r", stdin);
    freopen("$OUTFILE", "w", stdout);
}

void quit () {
    fflush(stdout);
    exit(0);
}

int main (int argc, char ** argv) {
    init();
    quit();
}
